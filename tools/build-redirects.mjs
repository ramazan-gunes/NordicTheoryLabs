import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const contentRoot = path.join(root, "content", "blog");
const redirectsPath = path.join(root, "_redirects");

const languages = ["ar", "bs", "es", "fa", "fi", "ku", "pl", "ru", "so", "sv", "tr"];
const manualAliases = [
  ["/blog/ru/peshekhody-velosipedy-v-shvetsii", "/blog/ru/peshekhody-i-velosipedisty-v-shvetsii.html"],
  ["/blog/ru/peshekhody-velosipedy-shvetsiya", "/blog/ru/peshekhody-i-velosipedisty-v-shvetsii.html"],
  ["/blog/sv/landsavag-vilt", "/blog/sv/landsvagar-vilt.html"],
  ["/blog/sv/landsvag-vilt", "/blog/sv/landsvagar-vilt.html"],
];

async function walk(dir, files = []) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) await walk(full, files);
    else if (entry.name.endsWith(".md")) files.push(full);
  }
  return files;
}

function parseMarkdown(text) {
  const match = text.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n?/);
  if (!match) return null;
  const meta = {};
  for (const line of match[1].split(/\r?\n/)) {
    const pair = line.match(/^([A-Za-z0-9_-]+):\s*"(.*)"\s*$/);
    if (pair) meta[pair[1]] = pair[2];
  }
  return meta.slug && meta.language ? meta : null;
}

function blogPathFor(post) {
  return post.language === "en"
    ? `/blog/${post.slug}.html`
    : `/blog/${post.language}/${post.slug}.html`;
}

function extensionlessFor(post) {
  return blogPathFor(post).replace(/\.html$/, "");
}

function addRedirect(lines, source, target) {
  if (source === target) return;
  lines.set(source, `${source} ${target} 301`);
}

async function main() {
  const files = (await walk(contentRoot)).filter((file) => !file.endsWith("_post-template.md"));
  const posts = [];
  for (const file of files) {
    const meta = parseMarkdown(await fs.readFile(file, "utf8"));
    if (meta) posts.push(meta);
  }

  const byTranslation = new Map();
  for (const post of posts) {
    const key = post.translation_of || post.slug;
    if (!byTranslation.has(key)) byTranslation.set(key, []);
    byTranslation.get(key).push(post);
  }

  const lines = new Map();

  addRedirect(lines, "/blog", "/blog/");
  addRedirect(lines, "/blog/en", "/blog/");
  addRedirect(lines, "/blog/en/", "/blog/");
  for (const lang of languages) addRedirect(lines, `/blog/${lang}`, `/blog/${lang}/`);

  for (const post of posts) {
    const canonical = blogPathFor(post);
    if (post.language === "en") {
      addRedirect(lines, `/blog/en/${post.slug}`, canonical);
      addRedirect(lines, `/blog/en/${post.slug}/`, canonical);
      addRedirect(lines, `/blog/en/${post.slug}.html`, canonical);
    }
  }

  for (const group of byTranslation.values()) {
    const english = group.find((post) => post.language === "en");
    if (!english) continue;
    for (const post of group) {
      if (post.language === "en") continue;
      const canonical = blogPathFor(post);
      const legacyBase = `/blog/${post.language}/${english.slug}`;
      addRedirect(lines, legacyBase, canonical);
      addRedirect(lines, `${legacyBase}/`, canonical);
      addRedirect(lines, `${legacyBase}.html`, canonical);
    }
  }

  const bosnianPosts = posts.filter((post) => post.language === "bs");
  for (const post of bosnianPosts) {
    const canonical = blogPathFor(post);
    const legacyBase = `/blog/sr/${post.slug}`;
    addRedirect(lines, legacyBase, canonical);
    addRedirect(lines, `${legacyBase}/`, canonical);
    addRedirect(lines, `${legacyBase}.html`, canonical);
  }

  for (const [source, target] of manualAliases) {
    addRedirect(lines, source, target);
    addRedirect(lines, `${source}/`, target);
    addRedirect(lines, `${source}.html`, target);
  }

  const body = [
    "# Generated by tools/build-redirects.mjs",
    "# Canonical cleanup for old blog URL variants reported by Search Console.",
    "# Host-level HTTP -> HTTPS and www -> apex redirects must be configured in Cloudflare Bulk Redirects/Rules.",
    "",
    ...[...lines.values()].sort(),
    "",
    "# Dynamic extensionless article cleanup. Keep after static legacy mappings.",
    "/blog/:slug /blog/:slug.html 301",
    "/blog/:slug/ /blog/:slug.html 301",
    "/blog/:lang/:slug /blog/:lang/:slug.html 301",
    "/blog/:lang/:slug/ /blog/:lang/:slug.html 301",
    "",
  ].join("\n");

  await fs.writeFile(redirectsPath, body, "utf8");
  console.log(JSON.stringify({ redirects: lines.size, file: "_redirects" }, null, 2));
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
